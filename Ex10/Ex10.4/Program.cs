//Desenvolver um algoritmo para converter a temperatura em graus Celsius para Fahrenheit, a
//fórmula é F = (9 * C + 160) / 5;

Console.WriteLine("Insira a temperatura em graus Celsius:");
double C = double.Parse(Console.ReadLine());
double F = (9 * C + 160) / 5;

Console.WriteLine($"A temperatura em graus Fahrenheit é: {F}ºF");







